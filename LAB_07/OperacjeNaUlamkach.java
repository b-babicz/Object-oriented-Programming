package matematyka;

public class OperacjeNaUlamkach {

   public static float dodawanieUlamkow(int a, int b, int c, int d)
    {
        return (a/b)+(c/d);
    }
    public static float odejmowanieUlamkow(int a, int b, int c, int d)
    {
        return (a/b)-(c/d);
    }
    public static float mnozenieUlamkow(int a, int b, int c, int d)
    {
        return (a/b)*(c/d);
    }
    public static float mnozenieUlamkaPrzezCalkowita(int a, int b, int x)
    {
        return (a/b)*x;
    }
    public static float dzielenieUlamkow(int a, int b, int c, int d)
    {
        return (a/b)/(c/d);
    }



}
