package matematyka;

public class OperacjeNaLiczbachZespolonych {


    public static void dodawanieLiczbZespolonych(int a, int b, int c, int d)
    {
        System.out.println((a+c) + " + " + (b+d)+"I");
    }
    public static void odejmowanieLiczbZespolonych(int a, int b, int c, int d)
    {
        System.out.println((a-c) + " + " + (b-d)+"I");
    }
    public static void mnozenieLiczbZespolonych(int a, int b, int c, int d)
    {
        System.out.println((a*c) + " + " + (b*d)+"I");
    }
    public static void mnozenieLiczbyZespolonejPrzezRzeczywsta(int a, int b, int x)
    {
        System.out.println((a*x) + " + " + (b*x)+"I");
    }
    public static void dzielenieLiczbZespolonych(int a, int b, int c, int d)
    {
        System.out.println((a/c) + " + " + (b/d)+"I");
    }

}
